using namespace std;
class Node
{
public:
    int val;
    Node *next;
    Node *prev;

    Node(int val)
    {
        this->val = val;
        this->next = NULL;
        this->prev = NULL;
    }
};

void insert_tail(Node *&head, Node *&tail, int value)
{
    Node *newNode = new Node(value);
    if (tail == NULL)
    {
        head = newNode;
        tail = newNode;
        return;
    }
    tail->next = newNode;
    newNode->prev = tail;
    tail = tail->next;
}

int size(Node *head)
{
    Node *temp = head;
    int count = 0;
    while (temp != NULL)
    {
        count++;
        temp = temp->next;
    }
    return count;
}

void palindrome(Node *head, Node *tail)
{
    Node *i = head;
    Node *j = tail;
    bool flag = true;
    while (i != j && i->next != j)
    {
        if (i->val == j->val)
        {
            i = i->next;
            j = j->prev;
            continue;
        }
        else
        {
            flag = false;
            break;
        }
    }
    // cout << i->val << " " << j->val;
    if (i->val == j->val)
    {
        flag = true;
    }
    else
    {
        flag = false;
    }

    if (flag)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

void print_original(Node *head)
{
    Node *temp = head;
    while (temp != NULL)
    {
        cout << temp->val << " ";
        temp = temp->next;
    }
    cout << endl;
}
